function buildMenu() {
  const role = localStorage.getItem("role");
  let html = "";

  if (role === "ADMIN") {
    html += `<span class="sidebar-section-label">Admin</span>
      <a class="menu" href="/pages/admin/dashboard.html">${IC.dashboard} Dashboard</a>
      <a class="menu" href="/pages/admin/users.html">${IC.users} Users</a>
      <a class="menu" href="/pages/admin/departments.html">${IC.departments} Departments</a>
      <a class="menu" href="/pages/admin/visitors.html">${IC.visitors} Visitors</a>`;
  }

  if (role === "HOST") {
    html += `<span class="sidebar-section-label">Host</span>
      <a class="menu" href="/pages/host/pending.html">${IC.pending} Pending Visitors</a>
      <a class="menu" href="/pages/host/visitors.html">${IC.visitors} My Visitors</a>`;
  }

  if (role === "SECURITY_GUARD") {
    html += `<span class="sidebar-section-label">Security</span>
      <a class="menu" href="/pages/security/register.html">${IC.register} Register Visitor</a>
      <a class="menu" href="/pages/security/checkin.html">${IC.checkin} Check In</a>
      <a class="menu" href="/pages/security/checkout.html">${IC.checkout} Check Out</a>
      <a class="menu" href="/pages/security/today.html">${IC.visitors} Today's Visitors</a>`;
  }

  // My Profile — available to every role
  html += `<span class="sidebar-section-label" style="margin-top:8px;">Account</span>
    <a class="menu" href="/pages/profile.html">${IC.profile} My Profile</a>`;

  document.getElementById("menu").innerHTML = html;
}

function logout() {
  localStorage.clear();
  location = "../../login.html";
}

// ── Toast helper (used by all pages) ──────────────────────────
function showToast(message, type = "success") {
  let container = document.querySelector(".toast-container");
  if (!container) {
    container = document.createElement("div");
    container.className = "toast-container";
    document.body.appendChild(container);
  }

  const successIcon = `<svg class="toast-icon" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" d="M9 12.75L11.25 15 15 9.75M21 12a9 9 0 11-18 0 9 9 0 0118 0z"/></svg>`;
  const errorIcon   = `<svg class="toast-icon" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="2" stroke="currentColor"><path stroke-linecap="round" stroke-linejoin="round" d="M12 9v3.75m9-.75a9 9 0 11-18 0 9 9 0 0118 0zm-9 3.75h.008v.008H12v-.008z"/></svg>`;

  const toast = document.createElement("div");
  toast.className = `toast ${type}`;
  toast.innerHTML = `${type === "success" ? successIcon : errorIcon} ${message}`;
  container.appendChild(toast);
  setTimeout(() => toast.remove(), 3500);
}
