async function loadComponent(element, file) {
  const response = await fetch(file);
  document.querySelector(element).innerHTML = await response.text();
}

async function setupLayout() {
  await loadComponent("#sidebar", "../../components/sidebar.html");
  await loadComponent("#navbar",  "../../components/navbar.html");

  const username = localStorage.getItem("username") || "User";
  const role     = localStorage.getItem("role") || "";

  buildMenu();

  document.getElementById("username").innerText  = username;
  document.getElementById("userInitial").innerText = username.charAt(0).toUpperCase();

  const roleEl = document.getElementById("roleLabel");
  if (roleEl) {
    roleEl.innerText = role.replace("_", " ");
  }

  // Highlight active link
  const path = window.location.pathname;
  document.querySelectorAll(".menu").forEach(link => {
    if (link.href && link.href.includes(path)) link.classList.add("active");
  });
}

